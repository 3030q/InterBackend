package ru.internaft.backend.SpringAppCache;

public class ReturnedDocument {
    private String name;

    public ReturnedDocument(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    private int id;

}
